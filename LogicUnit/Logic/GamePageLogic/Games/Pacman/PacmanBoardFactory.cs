        public int[,] BuildBoardMatrix(int m_Width, int m_Height)
        {
            int[,] mat = new int[m_Width,m_Height];

            for (int r = 0; r < m_Height; r++)
            {
                for (int c = 0; c < m_Width; c++)
                {
                    mat[c,r] = m_grid[r, c];

                    if ((r == 0 || c == 0 || r == m_Height - 1 || c == m_Width - 1) && m_grid[r, c] == 1)
                    {
                        mat[c,r ] = 0;
                    }
                }
            }

            return mat;
        }
